// ── Main ───────────────────────────────────────────────────────────

async function main() {
  console.log('Connecting to Cosmos DB...');
  const { database } = await client.databases.createIfNotExists({ id: DATABASE_ID });
  console.log(`Database "${DATABASE_ID}" ready.`);

  const { container } = await database.containers.createIfNotExists({
    id: CONTAINER_ID,
    partitionKey: { paths: ['/id'] },
  });
  console.log(`Container "${CONTAINER_ID}" ready.`);

  console.log(`\nUpserting ${patients.length} patients...\n`);

  for (const patient of patients) {
    const { resource } = await container.items.upsert(patient);
    console.log(`  ✓ ${resource.mrn}  ${resource.firstName} ${resource.lastName}`);
  }

  console.log('\nDone! All 10 VSP patients seeded.');
}

main().catch((err) => {
  console.error('Seed failed:', err);
  process.exit(1);
});
